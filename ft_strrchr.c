#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int		i;
	char	a;

	i = ft_strlen(str);
	a = c;
	if (a == 0)
		return ((char *)&str[i]);
	while (i >= 0)
	{
		if (str[i] == c)
			return ((char *)&str[i]);
		i--;
	}
	return (NULL);
}
